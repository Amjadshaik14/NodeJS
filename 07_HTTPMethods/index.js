// HTTP Methods
// GET, POST, PUT, PATCH, DELETE
// GET -- When you search for something in browser, it create a GET request
// POST -- When you want to send and mutate some data in server
// PUT -- Updates and existing resource or creates if it doesn't exist
// PATCH -- Updates part of an existing resource
// DELETE -- Deletes a resource from the server
// HEAD -- Similar to GET but only retrieves the headers of a response